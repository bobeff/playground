# A pipe has a 3/8 inch diameter. Express the diameter in centimeters.

const centimetersPerInch = 2.54
const pipeDiameterInInches = 3 / 8

const pipeDiameterInCentimeters = pipeDiameterInInches * centimetersPerInch

echo "The pipe diameter is ", pipeDiameterInInches, " inches."
echo "The pipe diameter is ", pipeDiameterInCentimeters, " centimeters."
